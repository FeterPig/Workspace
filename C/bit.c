#include <stdio.h>

int main()
{
    /**
     * 源码: 00000000000000000000000000000011
     * 反码: 00000000000000000000000000000011
     * 补码: 00000000000000000000000000000011
     */
    int a = 3;

    /**
     * 源码: 10000000000000000000000000000101
     * 反码: 11111111111111111111111111111010
     * 补码: 11111111111111111111111111111011
     */
    int b = -5;

    /**
     * 内存中储存的是补码
     * 3的补码:  00000000000000000000000000000011
     * -5的补码: 11111111111111111111111111111011
     *
     * 按位与: 同1为1, 否则为0
     * 按位或: 有1为1, 同0为0
     * 按位异或: 相同为0, 不同为1
     */

    /**
     * 补码: 00000000000000000000000000000011
     * 反码: 00000000000000000000000000000011
     * 源码: 00000000000000000000000000000011
     * 十进制: 3
     */
    printf("%d & %d = %d\n", a, b, a & b);

    /**
     * 补码: 11111111111111111111111111111011
     * 反码: 11111111111111111111111111111010
     * 源码: 10000000000000000000000000000101
     * 十进制: -5
     */
    printf("%d | %d = %d\n", a, b, a | b);

    /**
     * 补码: 11111111111111111111111111111000
     * 反码: 11111111111111111111111111110111
     * 源码: 10000000000000000000000000001000
     * 十进制: -8
     */
    printf("%d ^ %d = %d\n", a, b, a ^ b);

    return 0;
}