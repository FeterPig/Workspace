#include <stdio.h>

int main()
{
    int a = 0;
    int b = 0;

    printf("输入a b: ");
    scanf("%d %d", &a, &b);

    /**
     * a = 3: 00000000000000000000000000000011 (补码)
     * b = 5: 00000000000000000000000000000101 (补码)
     */
    printf("交换前: a = %d , b = %d\n", a, b);

    /**
     * a ^ b: 00000000000000000000000000000110 (6)
     * a = a ^ b = 6
     * a = 6: 00000000000000000000000000000110
     * b = 5: 00000000000000000000000000000101
     */
    a = a ^ b;
    printf("第一步: a = %d , b = %d\n", a, b);

    /**
     * a ^ b: 00000000000000000000000000000011 (3)
     * b = a ^ b = 3
     * a = 6: 00000000000000000000000000000110
     * b = 3: 00000000000000000000000000000011
     */
    b = a ^ b;
    printf("第二步: a = %d , b = %d\n", a, b);

    /**
     * a ^ b: 00000000000000000000000000000101 (5)
     * a = a ^ b = 5
     * a = 5: 00000000000000000000000000000101
     * b = 3: 00000000000000000000000000000011
     */
    a = a ^ b;
    printf("交换后: a = %d , b = %d\n", a, b);

    return 0;
}